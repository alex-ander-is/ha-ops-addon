LANGUAGES = {DEFAULT_LANGUAGE: EN_TEXT}


class CatalogError(RuntimeError):
    def __init__(self, key, **values):
        super().__init__(key)
        self.key = key
        self.values = values

    def __str__(self):
        return t(self.key, **self.values)


def t(key, language=DEFAULT_LANGUAGE, **values):
    library = LANGUAGES.get(language, EN_TEXT)
    template = library.get(key, EN_TEXT.get(key, key))
    return template.format(**values) if values else template


def error(key, **values):
    return CatalogError(key, **values)


def user_message(exc, fallback_key="error.request_failed"):
    if isinstance(exc, CatalogError):
        return str(exc)
    return t(fallback_key)
